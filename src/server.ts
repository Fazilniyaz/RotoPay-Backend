// Server Entry Point
// Starts the HTTP server, connects DB, verifies mailer
// в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

import { env } from "./utilities/env";
import { prisma } from "./utilities/prisma.client";
import { verifyMailerConnection } from "./utilities/mailer";
import { startScheduler } from "./services/scheduler.service";
import app from "./app";

async function startServer(): Promise<void> {
  try {
    // в”Ђв”Ђ 1. Test Database Connection в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
    console.log(" [Server] Connecting to MongoDB...");
    await prisma.$connect();
    console.log(" [Server] MongoDB connected");

    // в”Ђв”Ђ 2. Verify Mailer в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
    await verifyMailerConnection();

    // в”Ђв”Ђ 3. Start background scheduler (auto clock-in/out, reminders) в”Ђв”Ђ
    startScheduler();

    // в”Ђв”Ђ 4. Start Express в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
    const server = app.listen(env.PORT, () => {
      console.log("\nв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ");
      console.log(`рџљЂ RotaPay API running in ${env.NODE_ENV} mode`);
      console.log(`рџЊђ http://localhost:${env.PORT}`);
      console.log(`рџЏӨ Health: http://localhost:${env.PORT}/health`);
      console.log("в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n");
    });

    // в”Ђв”Ђ 4. Graceful Shutdown в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
    const shutdown = async (signal: string): Promise<void> => {
      console.log(`\nвљ пёЏ  [Server] ${signal} received вЂ” shutting down gracefully`);
      server.close(async () => {
        await prisma.$disconnect();
        console.log("вњ… [Server] MongoDB disconnected");
        console.log("рџ‘‹ [Server] Server closed");
        process.exit(0);
      });
    };

    process.on("SIGTERM", () => shutdown("SIGTERM"));
    process.on("SIGINT", () => shutdown("SIGINT"));

    // в”Ђв”Ђ 5. Unhandled Rejections в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
    process.on("unhandledRejection", (reason) => {
      console.error("рџ”Ө [Server] Unhandled rejection:", reason);
      process.exit(1);
    });

    process.on("uncaughtException", (error) => {
      console.error("[Server] Uncaught exception:", error);
      process.exit(1);
    });

  } catch (error) {
    console.error("[Server] Failed to start:", error);
    await prisma.$disconnect();
    process.exit(1);
  }
}

startServer();
